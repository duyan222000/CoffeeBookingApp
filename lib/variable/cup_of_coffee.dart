class CupOfCoffee {
  final String name;
  final String shot;
  final bool hot;
  final String size;
  final String ice;
  final DateTime buyDate;
  final bool onGoing;
  final String deliveredLocation;
  final int cost;
  CupOfCoffee(
      {required this.name,
      required this.shot,
      required this.hot,
      required this.size,
      required this.ice,
      required this.buyDate,
      required this.onGoing,
      required this.deliveredLocation,
      required this.cost});
}
